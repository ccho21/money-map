  async getRecommendedKeywords(
    userId: string,
    limit = 5,
    sampleSize = 100,
  ): Promise<string[]> {
    const recent = await this.prisma.transaction.findMany({
      where: { userId, note: { not: null } },
      select: { note: true },
      orderBy: { date: 'desc' },
      take: sampleSize,
    });

    const freq: Record<string, number> = {};
    for (const { note } of recent) {
      note!
        .toLowerCase()
        .replace(/[^\p{L}\p{N}\s]/gu, '') // ÌäπÏàòÎ¨∏Ïûê Ï†úÍ±∞(Ïú†ÎãàÏΩîÎìú ÏßÄÏõê)
        .split(/\s+/)
        .forEach((w) => {
          if (w.length < 3) return; // 3Ïûê ÎØ∏Îßå Ï†úÏô∏
          freq[w] = (freq[w] ?? 0) + 1;
        });
    }

    return Object.entries(freq)
      .sort((a, b) => b[1] - a[1])
      .slice(0, limit)
      .map(([word]) => word);
  }

