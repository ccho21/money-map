  async recalculateAccountBalance(accountId: string) {
    const account = await this.prisma.account.findUnique({
      where: { id: accountId },
    });
    if (!account) throw new NotFoundException('Í≥ÑÏ¢åÎ•º Ï∞æÏùÑ Ïàò ÏóÜÏäµÎãàÎã§.');

    const transactions = await this.prisma.transaction.findMany({
      where: {
        OR: [
          { accountId },
          { toAccountId: accountId }, // Ïπ¥Îìú ÏûÖÍ∏à Í≥†Î†§
        ],
      },
    });

    let newBalance = 0;

    for (const tx of transactions) {
      if (tx.type === 'income' && tx.accountId === accountId) {
        newBalance += tx.amount;
      } else if (tx.type === 'expense' && tx.accountId === accountId) {
        newBalance -= tx.amount;
      } else if (tx.type === 'transfer') {
        if (tx.accountId === accountId && tx.toAccountId) {
          // Ï∂úÍ∏à
          newBalance -= tx.amount;
        } else if (tx.toAccountId === accountId) {
          // ÏûÖÍ∏à
          newBalance += tx.amount;
        }
      }
    }

    await this.prisma.account.update({
      where: { id: accountId },
      data: { balance: newBalance },
    });

    return newBalance;
  }

